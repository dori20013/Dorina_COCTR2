
public class Pont {

    private float X;
    private float Y;

    public Pont(float X, float Y) {
        this.X = X;
        this.Y = Y;
    }

    public float getX() {
        return X;
    }

    public float getY() {
        return Y;
    }

    public void setX(float X) {
        this.X = X;
    }

    public void setY(float Y) {
        this.Y = Y;
    }

}
